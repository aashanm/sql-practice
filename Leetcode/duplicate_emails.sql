Write a SQL query to find all duplicate emails in a table named Person.

+----+---------+
| Id | Email   |
+----+---------+
| 1  | a@b.com |
| 2  | c@d.com |
| 3  | a@b.com |
+----+---------+
For example, your query should return the following for the above table:

+---------+
| Email   |
+---------+
| a@b.com |
+---------+

--------------------------------------------------------------------------------------

SELECT Email
FROM Person

# group results by email
GROUP BY Email

# duplicate values would have count greater than 1 so filter by that
HAVING COUNT(*) > 1
